public class Student {
    private int Student_Id;
    private String Name;

    public void setStudent_Id(int id) {
        this.Student_Id = id;
    }

    public int getStudent_Id() {
        return Student_Id;
    }

    public void setName(String name) {
        this.Name = name;
    }

    public String getName() {
        return Name;
    }
}
